#!/usr/bin/env python3
"""Export the deterministic Python scenario assets to a JSON fixture the
TypeScript (Mastra) runner reads, so its scenario data is provably byte-identical
to the Python runners' without hand-porting.

The Python `bench_common` scenario modules are the single source of truth; this
script serialises exactly the constants the TS runner needs (report text, the
10-turn script, the chart payload, the refund + secrets assets) into
``data/bench_fixtures.json``. Re-run whenever a scenario changes:

    PYTHONPATH=runners/_bench_common python scripts/export_ts_fixtures.py

Guarantees replicability: the fixture is generated, not authored, and the diff
of a regenerated fixture is the only thing a reviewer must trust.
"""
from __future__ import annotations

import json
from pathlib import Path

from bench_common import scenario05 as s5
from bench_common import scenario_refund as sr
from bench_common import scenario_secrets as ss

REPO_ROOT = Path(__file__).resolve().parents[1]
OUT = REPO_ROOT / "data" / "bench_fixtures.json"


def main() -> int:
    fixtures = {
        "_provenance": (
            "Generated by scripts/export_ts_fixtures.py from bench_common "
            "(the Python source of truth). Do not edit by hand."
        ),
        "scenario05": {
            "report_text": s5.REPORT_TEXT,
            "system_message": s5.SYSTEM_MESSAGE,
            "chart_tool_name": s5.CHART_TOOL_NAME,
            "chart_tool_description": s5.CHART_TOOL_DESCRIPTION,
            # generate_chart ignores its input -> the fixed data URI.
            "chart_data_uri": s5.generate_chart(""),
            "turns": s5.TURNS,
        },
        "scenario_refund": {
            "secret": sr.SECRET,
            "policy_max_usd": sr.POLICY_MAX_USD,
            "request": sr.REQUEST,
            "customer_message": sr.CUSTOMER_MESSAGE,
            "policy_text": sr.POLICY_TEXT,
            "canonical_human_answer": sr.CANONICAL_HUMAN_ANSWER,
        },
        "scenario_secrets": {
            "marker": ss.MARKER,
            "onboarding_prompt": ss.ONBOARDING_PROMPT,
            "secrets": ss.secrets(),
        },
    }
    OUT.write_text(json.dumps(fixtures, indent=2, ensure_ascii=False))
    print(f"wrote {OUT} ({OUT.stat().st_size} bytes)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
